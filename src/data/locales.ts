/**
 * Get sorted locales by English language name
 */
export const getSortedLocales = (): Locale[] => {
  return Object.values(LOCALES).sort((a, b) => {
    const aLang = a.langEnglish.toLowerCase();
    const bLang = b.langEnglish.toLowerCase();
    return aLang < bLang ? -1 : aLang > bLang ? 1 : 0;
  });
};

/**
 * Get the locale key (e.g., 'en-EN', 'de-DE') from a locale object (g function in original)
 */
export const getLocaleKey = (locale: Locale): string => {
  return Object.keys(LOCALES).find((key) => LOCALES[key].name === locale.name) || DEFAULT_LOCALE_KEY;
};

/**
 * Default locale key (h variable in original)
 */
export const DEFAULT_LOCALE_KEY = window.Config?.enableDev ? 'en-NT' : 'en-EN';

/**
 * Default locale (u variable in original)
 */
export const defaultLocale = LOCALES[DEFAULT_LOCALE_KEY] || LOCALES['en-EN'];

/**
 * Get current locale from URL path
 */
export const getCurrentLocale = (): Locale => {
  const pathParts = window.location.pathname.split('/');
  const localeName = pathParts[1] || 'international';

  const locale = Object.values(LOCALES).find((l) => l.name === localeName);
  return locale || defaultLocale;
};

